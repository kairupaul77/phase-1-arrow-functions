// Function expression for divide
const divide = function(a, b) {
    return a / b;
  };
  
  // Arrow function for square
  const square = (x) => {
    return x * x;
  };
  
  // Arrow function for add
  const add = (a, b) => {
    return a + b;
  };
  
  // Exporting the functions
  module.exports = { divide, square, add };